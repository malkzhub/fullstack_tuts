//// Arithmetic
// Addition
console.log(2 + 3) // 5

// Subtraction
console.log(2 - 3) // -1

// Multiplication
console.log(2 * 3) // 6

// Division
console.log(3 / 2) // 1.5

// Modulus
console.log(3 % 2) // 1

// Exponentiation
console.log(3 ** 3) // 27

// Other Samples
console.log(100 + 4 * 11) // 144
console.log((100 + 4) * 11) // 1144

// Special Numbers
console.log(Infinity);
console.log(-Infinity);
console.log(Infinity - 1); // Infinity
console.log(0 / 0) // NaN - not a number
console.log(Infinity - Infinity) // NaN

